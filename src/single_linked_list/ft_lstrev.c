#include "libft.h"

void	ft_lstrev(t_list **begin_list)
{
	t_list *current;
	t_list *prev;
	t_list *next;

	current = *begin_list;
	next = 0;
	prev = 0;
	while (current)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}
	*begin_list = prev;
}
